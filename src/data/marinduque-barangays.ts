/** Flat list of all barangays across all municipalities */
export const ALL_BARANGAYS: string[] = Object.values(MARINDUQUE_BARANGAYS).flat();

/** Get barangays for a specific municipality, or all if unspecified */
export function getBarangays(municipality?: string): string[] {
  if (municipality && MARINDUQUE_BARANGAYS[municipality]) {
    return MARINDUQUE_BARANGAYS[municipality];
  }
  return ALL_BARANGAYS;
}
